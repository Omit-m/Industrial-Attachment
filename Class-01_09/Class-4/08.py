# Comparison Operator

#  a , b
# a > b [ a greater than b]
# a < b [ a less than b]
# a >= b [ a greater than or equal b]
# a <= b [ a less than or equal b]
# a != b [ a not equal b]
# a == b [ a and b is euqal]

